class Bootcamp

    def initialize(name, slogan, teachers, students, grades)
        @name = name
        @slogan = slogan
        @teachers = teachers
        @students = students
        @grades = grades
    end

    #getter methods

    def name; @name end
    def slogan; @slogan end
    def teachers; @teachers end
    def students; @students end
    def grades; @grades end

    

    
end
